     <p>Galleries are a very popular method to incorporate images and graphic media into your website.  They give the owner/developer a way to showcase their selected merchandise, services or express visual impact for your content.  The user can efficiently view several images clearly and concisely without having to navigate all over the site.  Galleries can be static pages of thumbnail images, dynamic flash, or carousel/slider styles.</p>

     <p>It's important to make your gallery accessible.  Remember that many users may have various disabilities so images must have text alternatives to describe the information represented by them.  The w3c Web Accessibility Initiative (WAI) provides a great guideline for this.(w3c, Eggert, Eric, Abou-Zahra, Shadi et al.)</p>

     <p>Whether to integrate a gallery into your site's design should be a careful and strategic decision.  Everyone loves looking at visuals and pictures, but you want to avoid 'visual overload' that is pointless or distracts from your other pertinent content.  Galleries should complement your site, not overtake it!

     <p>A good photo gallery should create an emotive response in the viewer.  It's presence should give your site added credibility and "proof' of the quality of your goods and services.  It should basically excite the user and act as a prime CTA (Call To Action).</p>

     <p>Several studies confirm that 65% of the human population are visual learners so graphics and visual content are crucial to learning and marketing. One of their cited studies shows that after three days, a user will retain 10-20% of written or spoken information but almost 65% of visual information. (shiftELearning.com)

     <p>There are many types and styles of image galleries you could use.  Most currently popular galleries are created using varying combinations of Javascript, CSS, HTML code.

     <p>I particularly like the 'best practices' advice offered by UCI Digital:

          <ul> 
               <li>Choose high-quality images.</li>
               <li>Find a layout that fits your brand.</li>
               <li>Keep your image gallery current.</li>
               <li>Ensure that your gallery is compatible across current browsers.</li>
               <li>Consider syncing your gallery content with your social media accounts.</li>
               <li>Don't be afraid to experiment.</li>
               <li>Build with responsive design and mobile-first consideration.</li>
               <li>Optimize image file size and properly size them to work with your webpage's available space.(see also Mark, Allen)</li>
               <li>Above all, keep it simple to ensure a great user experience!</li>
          </ul>
     </p>

