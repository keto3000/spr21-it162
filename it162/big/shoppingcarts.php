     <p>The goal of an e-commerce site is to sell products.  DigitalCommerce360 estimates that consumers spent, globally, $4.29 trillion online in 2020, up a whopping 24.1% increase over the $3.46 trillion they spent in 2019.(Young, Jessica)</p>

     <p>A well designed e-commerce website should provide a natural user experience that is easy to browse, search, and complete the checkout process.</p>

     <p>The key component of any eCommerce site is its ‘Shopping Cart.’  The term evokes the old brick & mortar store experience for pushing around an actual cart and putting your potential purchase in it.  While that analogy still holds true for a digital shopping cart as well, the online version does so much more!</p>

     <p>The eCommerce Shopping Cart lets users select products, review and/or modify their selections, add extra items as needed and purchase the products.  It accepts the customer's payment and handles the payment processing of information to the merchant, etc.  Businesses can opt to host their own shopping carts (called Licensed/Self-Hosted) or sign up with a third-party hosting company to use their off-the shelf shopping cart package (Hosted) product.</p>

     <p>A big issue for eCommerce businesses is known as 'Shopping Cart abandonment.  Baymard Institute has published some interesting findings on this topic.</p>

     <p><img src="images/shopping-cart-abandonment.jpg" alt="Reasons for Shopping Cart Abandonment"></p>

     </p>According to their research, the online shopping cart abandonment rate is ~69.8%.  If you filter out the fairly sizable group of online shoppers that claim to abandon their carts because they were 'just browsing', the remaining reasons are still notable since they CAN be resolved by making design changes to improve the user experience.</p>

     <p>One key finding of the Baymard study: "...nearly 1 out of 5 shoppers have abandoned a cart in the last quarter due to a “too long / complicated checkout process”, yet for most checkouts it’s possible to make a 20-60% reduction in the number of form elements shown to users during the default checkout flow. (Baymard Institute)</p>

     <p>Reducing the number of forms and simplifying shopping cart information processing is important to improve your eCommerce site's success rate, but there are other design elements that are crucial to your site's success:
          <ul>
               <li>Shoppers want to see what they’re buying. Get them excited by providing demos and product views with high-quality photos, optimized sizing and different angles.</li>  

               <li> Bring your visitor in on an emotional level.  Online users want an authentic, immersive experience when they are going to spend $$ on your site.  They need to have confidence that their ‘before, during and after’ buying experience will be as seamless as possible.</li> 
     
               <li>Establishing a strong and consistent Brand ID across your business platforms--web, mobile, and physical store builds trust and consumer confidence.</li>
          </ul>
</p>

